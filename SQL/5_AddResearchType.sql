-- ============================================================
-- 5_AddResearchType.sql
-- Run this script in SSMS (ThesisRepositoryDB) to add
-- ResearchType to the Theses table.
-- All statements are idempotent (safe to re-run).
-- ============================================================

USE ThesisRepositoryDB;
GO

-- ??????????????????????????????????????????????????????????????????????????????
-- Add ResearchType column to Theses table
-- ??????????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE object_id = OBJECT_ID(N'[dbo].[Theses]')
      AND name = N'ResearchType'
)
BEGIN
    ALTER TABLE [dbo].[Theses]
        ADD [ResearchType] NVARCHAR(50) NULL;

    PRINT 'Column [ResearchType] added to [Theses].';
END
ELSE
BEGIN
    PRINT 'Column [ResearchType] already exists in [Theses] — skipped.';
END
GO

-- Verification: Show the updated Theses table schema
SELECT 
    COLUMN_NAME, 
    DATA_TYPE, 
    IS_NULLABLE
FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_NAME = 'Theses'
  AND COLUMN_NAME = 'ResearchType';
GO

PRINT '? Migration complete. [ResearchType] column is available in Theses.';
