-- ============================================================
-- 4_AddAuthorEmailsAndApaCitation.sql
-- Run this script in SSMS (ThesisRepositoryDB) to add author
-- email fields and APA citation storage to the Theses table.
-- All statements are idempotent (safe to re-run).
-- ============================================================

USE ThesisRepositoryDB;
GO

-- ??????????????????????????????????????????????????????????????????????????????
-- Add MainAuthorEmail column to Theses table
-- ??????????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE object_id = OBJECT_ID(N'[dbo].[Theses]')
      AND name = N'MainAuthorEmail'
)
BEGIN
    ALTER TABLE [dbo].[Theses]
        ADD [MainAuthorEmail] NVARCHAR(255) NULL;

    PRINT 'Column [MainAuthorEmail] added to [Theses].';
END
ELSE
BEGIN
    PRINT 'Column [MainAuthorEmail] already exists in [Theses] — skipped.';
END
GO

-- ??????????????????????????????????????????????????????????????????????????????
-- Add CoAuthorEmail column to Theses table
-- ??????????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE object_id = OBJECT_ID(N'[dbo].[Theses]')
      AND name = N'CoAuthorEmail'
)
BEGIN
    ALTER TABLE [dbo].[Theses]
        ADD [CoAuthorEmail] NVARCHAR(255) NULL;

    PRINT 'Column [CoAuthorEmail] added to [Theses].';
END
ELSE
BEGIN
    PRINT 'Column [CoAuthorEmail] already exists in [Theses] — skipped.';
END
GO

-- ??????????????????????????????????????????????????????????????????????????????
-- Add ApaCitation column to Theses table
-- ??????????????????????????????????????????????????????????????????????????????
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE object_id = OBJECT_ID(N'[dbo].[Theses]')
      AND name = N'ApaCitation'
)
BEGIN
    ALTER TABLE [dbo].[Theses]
        ADD [ApaCitation] NVARCHAR(MAX) NULL;

    PRINT 'Column [ApaCitation] added to [Theses].';
END
ELSE
BEGIN
    PRINT 'Column [ApaCitation] already exists in [Theses] — skipped.';
END
GO

-- Verification: Show the updated Theses table schema
SELECT 
    COLUMN_NAME, 
    DATA_TYPE, 
    IS_NULLABLE
FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_NAME = 'Theses'
ORDER BY ORDINAL_POSITION;
GO

PRINT '? Migration complete. All new columns have been added to the Theses table.';
